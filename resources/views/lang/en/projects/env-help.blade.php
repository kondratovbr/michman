<div>
    Here you may edit the <x-code>.env</x-code> file for your application,
    which is the default environment file that is loaded by packages like
    <x-code>django-environ</x-code> or <x-code>python-decouple</x-code>
    The file will not be readable by any users apart from the one running your application,
    so you can use it to store sensitive data, like a database password.
    If the application is uninstalled, the environment file will also be removed.
</div>
